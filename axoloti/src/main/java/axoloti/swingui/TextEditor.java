    public TextEditor(Property stringProperty, IModel model, DocumentWindow parent) {
        super(model, parent);
        this.stringProperty = stringProperty;
        initComponents();
        initComponents2();
    }

    private void initComponents2() {
        setJMenuBar(new StandardMenubar(null));
        textArea = new RSyntaxTextArea(20, 60);
        textArea.setSyntaxEditingStyle(SyntaxConstants.SYNTAX_STYLE_CPLUSPLUS);
        textArea.setCodeFoldingEnabled(true);
        RTextScrollPane sp = new RTextScrollPane(textArea);
        cp.setLayout(new BorderLayout());
        cp.add(sp);
        textArea.setVisible(true);
        setContentPane(cp);
        textArea.setText((String) this.stringProperty.get(model));
        textArea.addKeyListener(new KeyAdapter() {
            @Override
            public void keyTyped(KeyEvent e) {
                if ((e.getKeyChar() == '\n') && (e.isControlDown() || e.isMetaDown())) {
                    updateModel();
                }
            }
        });
        setVisible(true);
    }

    public void setText(String s) {
        textArea.setText(s);
    }

    String getText() {
        return textArea.getText();
    }

    @Override
    public void close() {
        dispose();
    }

    private void updateModel() {
        model.getController().generic_setModelUndoableProperty(stringProperty, textArea.getText());
    }

